def setup_proxychains():
    """Tulis config proxychains. INI HANYA MENULIS FILE CONFIG —
    tidak menjadikan proxychains aktif secara global. Proxychains
    tetap harus dipanggil manual sebagai prefix (proxychains4 <cmd>)
    atau lewat wrapper di tor_aliases.sh."""
    sudo = [] if is_root() else ['sudo']
    tmp = '/tmp/proxychains_tor.conf'
    with open(tmp, 'w') as f:
        f.write(PROXYCHAINS_CONFIG)

    wrote_any = False
    for path in ['/etc/proxychains4.conf', '/etc/proxychains.conf']:
        # Hanya tulis ke path yang relevan dengan binary yang terinstall,
        # tapi aman juga ditulis ke keduanya kalau ada.
        out, err, code = run_cmd(sudo + ['cp', tmp, path])
        if code == 0:
            wrote_any = True

    os.remove(tmp)
    if wrote_any:
        console.print("[green]✓ Proxychains dikonfigurasi (quiet_mode aktif, hanya dipakai saat dipanggil manual).[/green]")
    else:
        console.print("[red]✗ Gagal menulis config proxychains. Pastikan proxychains terinstall.[/red]")

def unsetup_proxychains():
    """Matikan config tanpa uninstall package — backup config lalu
    arahkan ProxyList ke target yang tidak ada efeknya (default localhost
    9050 tetap ditulis tapi file di-rename jadi .disabled agar proxychains4
    fallback ke default bawaan paket / gagal jelas, bukan diam-diam aktif)."""
    sudo = [] if is_root() else ['sudo']
    any_done = False
    for path in ['/etc/proxychains4.conf', '/etc/proxychains.conf']:
        if os.path.exists(path):
            backup = path + '.bak'
            run_cmd(sudo + ['cp', path, backup])
            run_cmd(sudo + ['rm', '-f', path])
            any_done = True
    if any_done:
        console.print("[yellow]✓ Config proxychains di-unsetup (backup disimpan dengan suffix .bak).[/yellow]")
        console.print("[dim]Binary proxychains masih terinstall, tapi tanpa config ia akan error kalau dipanggil — aman.[/dim]")
