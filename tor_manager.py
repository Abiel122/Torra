def tor_status():
    try:
        import socket
        s = socket.socket()
        s.settimeout(1)
        s.connect(('127.0.0.1', TOR_CONFIG['socks_port']))
        s.close()
        return True
    except Exception:
        pass
    if has_systemd():
        _, _, code = run_cmd(['systemctl', 'is-active', '--quiet', 'tor'])
        return code == 0
    return False

def proxychains_status():
    """True hanya kalau config proxychains ADA dan mengarah ke tor.
    Ini tidak berarti aktif global — proxychains cuma jalan kalau
    dipanggil manual sebagai prefix command (proxychains <cmd>)."""
    for path in ['/etc/proxychains.conf', '/etc/proxychains4.conf']:
        if os.path.exists(path):
            with open(path) as f:
                if '9050' in f.read():
                    return True
    return False

def _wait_tor_ready(prog, task, timeout=120):
    import socket
    start = time.time()
    elapsed = 0
    while elapsed < timeout:
        try:
            s = socket.socket()
            s.settimeout(0.5)
            s.connect(('127.0.0.1', TOR_CONFIG['socks_port']))
            s.close()
            return True
        except OSError:
            pass
        elapsed = int(time.time() - start)
        prog.update(task, description=f"Menunggu Tor siap... {elapsed}s")
        time.sleep(0.5)
    return False

def start_tor():
    sudo = [] if is_root() else ['sudo']
    ok = False

    with Progress(
        SpinnerColumn(), TextColumn("[progress.description]{task.description}"),
        console=console, transient=True
    ) as prog:
        task = prog.add_task("Memulai Tor...", total=None)

        if has_systemd():
            prog.update(task, description="Mengaktifkan & memulai Tor via systemctl...")
            run_cmd(sudo + ['systemctl', 'enable', 'tor'], check=False)
            out, err, code = run_cmd(sudo + ['systemctl', 'restart', 'tor'], check=False)
            if code != 0:
                prog.update(task, description="systemctl gagal, coba via 'service'...")
                run_cmd(sudo + ['service', 'tor', 'restart'], check=False)
        elif is_termux():
            prog.update(task, description="Menjalankan tor (Termux)...")
            subprocess.Popen(['tor'], stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        else:
            prog.update(task, description="Menjalankan tor langsung...")
            log_file = open('/tmp/tor_start.log', 'w')
            subprocess.Popen(['tor'], stdout=log_file, stderr=log_file)

        prog.update(task, description="Menunggu Tor siap di port 9050...")
        ok = _wait_tor_ready(prog, task, timeout=120)
        prog.update(task, description="[green]Tor aktif![/green]" if ok else "[red]Timeout.[/red]")

    if ok:
        console.print(f"[green]✓ Tor aktif di port {TOR_CONFIG['socks_port']}![/green]")
        return True
    console.print("[red]✗ Tor tidak merespons setelah 120 detik.[/red]")
    return False

def stop_tor():
    sudo = [] if is_root() else ['sudo']
    if has_systemd():
        out, _, code = run_cmd(['systemctl', 'is-active', '--quiet', 'tor'])
        if code == 0:
            run_cmd(sudo + ['systemctl', 'stop', 'tor'])
    run_cmd(sudo + ['pkill', '-x', 'tor'])
    console.print("[yellow]✓ Tor dihentikan.[/yellow]")

