def auto_rotate(interval=30, count=0):
    state = load_state()
    if state.get('circuit_locked'):
        console.print(Panel(
            "[red]Circuit sedang LOCKED. Auto-rotate akan terus minta konfirmasi tiap siklus, "
            "yang tidak praktis. Unlock dulu dari menu kalau mau auto-rotate jalan mulus.[/red]",
            border_style="red", title="🔒 Circuit Locked"
        ))
        if not Confirm.ask("Lanjutkan tetap (akan minta konfirmasi tiap rotasi)?", default=False):
            return

    console.print()
    console.rule(f"[bold cyan]🔄 Auto-Rotate setiap {interval}s[/bold cyan]")
    console.print("[dim]Tekan Ctrl+C untuk berhenti.[/dim]\n")

    counter = 0
    try:
        while True:
            if count > 0 and counter >= count:
                break
            counter += 1
            console.print(f"[bold cyan]─── Rotasi #{counter} ─ {datetime.now().strftime('%H:%M:%S')} ───[/bold cyan]")

            rotate_ip(force=True)
            new_info = get_ip_info()
            if new_info:
                console.print(
                    f"  IP Baru : [green bold]{new_info.get('ip')}[/green bold]"
                    f"  🌍 {new_info.get('country')} — {new_info.get('city')}"
                )

            if count == 0 or counter < count:
                for i in range(interval, 0, -1):
                    print(f"\r  [cyan]Rotasi berikutnya dalam {i}s...[/cyan]    ", end='', flush=True)
                    time.sleep(1)
                print()
    except KeyboardInterrupt:
        console.print("\n[yellow]Auto-rotate dihentikan.[/yellow]")

def change_country(code=None):
    if not code:
        t = Table(box=box.SIMPLE, show_header=False)
        t.add_column("Kode", style="bold cyan", width=6)
        t.add_column("Negara")
        for k, v in COUNTRIES.items():
            t.add_row(k, v)
        console.print(t)
        code = Prompt.ask("Kode negara").lower().strip()

    if code not in COUNTRIES:
        console.print(f"[red]Kode '{code}' tidak dikenal.[/red]")
        return False

    sudo = [] if is_root() else ['sudo']
    try:
        torrc_path = '/etc/tor/torrc'
        with open(torrc_path) as f:
            lines = f.readlines()

        new_lines = [l for l in lines if not l.startswith(('ExitNodes', 'ExcludeNodes'))]
        new_lines.append(f'ExitNodes {_fmt_countries(code)}\n')
        new_lines.append(f'ExcludeNodes {_fmt_countries(TOR_CONFIG["exclude_country"])}\n')

        tmp = '/tmp/torrc_new'
        with open(tmp, 'w') as f:
            f.writelines(new_lines)
        run_cmd(sudo + ['cp', tmp, torrc_path])
        os.remove(tmp)

        state = load_state()
        state['exit_country'] = code
        save_state(state)

        stop_tor()
        time.sleep(1)
        start_tor()

        console.print(f"[green]✓ Exit country diubah ke {COUNTRIES[code]}[/green]")
        console.print("[dim]Catatan: ganti negara = restart Tor = circuit baru, jadi IP exit juga baru "
                       "(meski di negara yang sama). Itu wajar.[/dim]")
        return True
    except Exception as e:
        console.print(f"[red]✗ Gagal: {e}[/red]")
        return False

