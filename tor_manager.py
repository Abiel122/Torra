IP_ENDPOINTS = [
    'https://api.ipify.org?format=json',
    'https://ifconfig.me/all.json',
    'https://icanhazip.com',
]

def _extract_ip(text, is_json):
    if is_json:
        try:
            data = json.loads(text)
            return data.get('ip') or data.get('ip_addr')
        except Exception:
            return None
    return text.strip() or None

def get_real_ip(timeout=8):
    for url in IP_ENDPOINTS:
        try:
            r = requests.get(url, timeout=timeout)
            if r.status_code != 200:
                continue
            is_json = 'json' in url
            ip = _extract_ip(r.text, is_json)
            if ip:
                return ip
        except Exception:
            continue
    return None

def get_tor_ip(timeout=15):
    if not tor_status():
        return None
    for url in IP_ENDPOINTS:
        try:
            r = requests.get(url, proxies=PROXIES_TOR, timeout=timeout)
            if r.status_code != 200:
                continue
            is_json = 'json' in url
            ip = _extract_ip(r.text, is_json)
            if ip:
                return ip
        except Exception:
            continue
    return None

def get_ip_info(use_tor=True, timeout=15):
    proxies = PROXIES_TOR if use_tor else None
    if use_tor and not tor_status():
        return None
    try:
        r = requests.get('https://ipinfo.io/json', proxies=proxies, timeout=timeout)
        if r.status_code == 200:
            return r.json()
    except Exception:
        pass
    return None

