def _find_cookie():
    for cp in ['/var/lib/tor/control_auth_cookie',
               '/run/tor/control.authcookie',
               '/tmp/tor/control.authcookie']:
        if os.path.exists(cp):
            try:
                with open(cp, 'rb') as f:
                    return f.read().hex()
            except PermissionError:
                pass
    return None

def _tor_send_signal(signal='NEWNYM'):
    import socket as _socket
    host = '127.0.0.1'
    port = TOR_CONFIG['control_port']
    cookie_hex = _find_cookie()

    with _socket.create_connection((host, port), timeout=5) as s:
        def send(cmd):
            s.sendall((cmd + '\r\n').encode())
            return s.recv(1024).decode(errors='replace')

        resp = send(f'AUTHENTICATE {cookie_hex}') if cookie_hex else send('AUTHENTICATE ""')
        if not resp.startswith('250'):
            raise RuntimeError(f'Auth gagal: {resp.strip()}')

        resp = send(f'SIGNAL {signal}')
        if not resp.startswith('250'):
            raise RuntimeError(f'Signal gagal: {resp.strip()}')
        send('QUIT')

